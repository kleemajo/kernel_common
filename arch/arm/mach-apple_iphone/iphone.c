inline int getCharPixel(int ch, int x, int y) {
	register int bitIndex = ((fontWidth * fontHeight) * ch) + (fontWidth * y) + x;
	return (fontData[bitIndex / 8] >> (bitIndex % 8)) & 0x1;
}

uint32_t PixelFromCoords(uint32_t x, uint32_t y) {
	return 0xFC00000 + 4 * ((y * 320) + x);
}

void clearScreen(void) {
	uint32_t end = PixelFromCoords(320, 480);
	int i=PixelFromCoords(0, 0);;
	while (i < end) {
		__raw_writel(0x0, IO_ADDRESS(i));
		i += 4;
	}
}

void scrollup888(void) {
	uint32_t newFirstLine = PixelFromCoords(0, fontHeight);
	uint32_t oldFirstLine = PixelFromCoords(0, 0);
	uint32_t end = oldFirstLine + 4 * (320 * 480);
	while(newFirstLine < end) {
		__raw_writel(__raw_readl(IO_ADDRESS(newFirstLine)),
				IO_ADDRESS(oldFirstLine));
		newFirstLine += 4;
		oldFirstLine += 4;
		//*(oldFirstLine++) = *(newFirstLine++);
	}
	while(oldFirstLine < end) {
		__raw_writel(0x0, IO_ADDRESS(oldFirstLine));
		oldFirstLine += 4;
		//*(oldFirstLine++) = 0x0;
	}
	Y--;
	//Y = 0;
}

void framebuffer_putc888(int c) {
	int TWidth = 320 / fontWidth;
	int THeight = 480 / fontHeight;
	if(c == '\r') {
		X = 0;
	} else if(c == '\n') {
		X = 0;
		Y++;
	} else {
		register uint32_t sx;
		register uint32_t sy;
		for(sy = 0; sy < fontHeight; sy++) {
			for(sx = 0; sx < fontWidth; sx++) {
				if(getCharPixel(c, sx, sy)) {
					__raw_writel(0xffffff, IO_ADDRESS(PixelFromCoords(sx +
									(fontWidth * X), sy + (fontHeight * Y))));
				} else {
					__raw_writel(0x000000, IO_ADDRESS(PixelFromCoords(sx +
									(fontWidth * X), sy + (fontHeight * Y))));
					/*
					__raw_writel(0xff00ff, IO_ADDRESS(0x0FC00000));
					__raw_writel(0xff00ff, IO_ADDRESS(0x0FC00004));
					//__raw_writel(0x100000, IO_ADDRESS(0x3C800000));
					//__raw_writel(0xff00ff, IO_ADDRESS(PixelFromCoords(0, 0)));
					__raw_writel(0xff00ff, IO_ADDRESS(PixelFromCoords(5, 0)));
					//__raw_writel(0xff00ff, IO_ADDRESS(PixelFromCoords(10, 0)));
					//__raw_writel(0xff00ff, IO_ADDRESS(PixelFromCoords(0, 10)));
					//__raw_writel(0xff00ff, IO_ADDRESS(PixelFromCoords(0, 20)));
					//__raw_writel(0xff00ff, IO_ADDRESS(PixelFromCoords(0, 30)));
					*/
				}
			}
		}

		X++;
	}

	if(X == TWidth) {
		X = 0;
		Y++;
	}

	if(Y == THeight) {
		scrollup888();
	}
}

